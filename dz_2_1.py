# Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2
# Странное задание

print('умножение целых чисел имеет тип',            '-', type(15 * 3))
print('деление чисел имеет тип',                    '-', type(15 / 3))
print('целочисленное деление целых чисел имеет тип','-', type(15 // 2))
print('возведение в степень целого числа имеет тип','-', type(15 ** 2))
